# basic data types
a_string = "text"
a_boolean = True
an_integer = 10
a_float = 5.28

print(type(a_string))
print(type(a_boolean))
print(type(an_integer))
print(type(a_float))

# long integer
print("*" * 10)
an_integer = 1_000_000_000
print(type(an_integer))

# float precision
print("*" * 10)
a_float = 0.8742742394729342
print(type(a_float))

#  negative
print("*" * 10)
an_integer = -100
a_float = -.58
print(an_integer)
print(a_float)

# type casting
print("*" * 10)
print(str(an_integer))
print(float(an_integer))
print(int(a_float))

